// Coder's Monday Morning Routine
function mondayMorning() {
    let coffeeCount = 0;
  
    // Step 1: Coffee
    function coffee() {
      coffeeCount++;
      console.log(`Coffee #${coffeeCount} brewed! ☕`); // Coffee #1 brewed! ☕
    }
  
    // Step 2: Reboot the system
    function rebootSystem() {
      console.log("Rebooting system... 💻"); // Rebooting system... 💻
    }
  
    // Step 3: Check GitHub notifications
    function checkNotifications() {
      console.log("Checking GitHub notifications... 🔔"); // Checking GitHub notifications... 🔔
    }
  
    // Step 4: Resolve last week's bugs
    function resolveBugs() {
      console.log("Debugging the mysterious bugs... 🐛"); // Debugging the mysterious bugs... 🐛
    }
  
    // Step 5: Merge conflicts
    function mergeConflicts() {
      console.log("Facing Monday morning merge conflicts... ⚔️");  // Facing Monday morning merge conflicts... ⚔️
    }
  
    // Final step: Work on new features (or not)
    function workOnFeatures() {
      console.log("Working on new features... after one more coffee. 😂");  // Working on new features... after one more coffee. 😂
    }
  
    // Execute the Monday routine
    coffee();
    rebootSystem();
    checkNotifications();
    resolveBugs();
    mergeConflicts();
    workOnFeatures();
  }
  
  // Start the Monday routine
  mondayMorning();
  